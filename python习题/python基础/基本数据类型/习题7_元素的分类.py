""""
    7、元素分类
        有如下值集合[11,22,33,44,55,66,77,88,99,90]，将所有大于66的值保存至字典的第一个key中，将小于66的值保存至第二个key的值中。
        即：{‘k1’:大于66的所有值, ‘k2’:小于66的所有值}。（编程题）
"""

lis = [11,22,33,44,55,66,77,88,99,90]
dic = {}
l1 = []
l2 = []
for i in lis:
    if i < 66:
        l1.append(i)
    elif i > 66:
        l2.append(i)
dic["k1"] = l1
dic["k2"] = l2
print(dic)






